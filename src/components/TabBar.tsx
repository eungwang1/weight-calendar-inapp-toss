import clsx from 'clsx';
import { haptic } from '../utils/haptic';
import style from './TabBar.module.css';

interface Props {
  activeTab: number;
  onTabChange: (tab: number) => void;
}

const TABS = [
  { label: '기록', icon: CalendarIcon },
  { label: '그래프', icon: GraphIcon },
];

export function TabBar({ activeTab, onTabChange }: Props) {
  return (
    <nav className={style.tabBar}>
      {TABS.map((tab, i) => (
        <button
          key={tab.label}
          className={clsx(style.tab, activeTab === i && style.active)}
          onClick={() => { haptic('tap'); onTabChange(i); }}
        >
          <tab.icon active={activeTab === i} />
          <span className={style.label}>{tab.label}</span>
        </button>
      ))}
    </nav>
  );
}

